setInterval(() => {
  const now = Date.now();
  const maxAge = 10 * 60 * 1000; // 10 –º–∏–Ω—É—Ç
  
  for (const [jobId, job] of JOBS.entries()) {
    if (now - job.createdAt.getTime() > maxAge) {
      JOBS.delete(jobId);
      JOB_LOGS.delete(jobId);
    }
  }
}, 10 * 60 * 1000);

